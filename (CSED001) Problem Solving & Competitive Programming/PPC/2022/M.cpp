int main(){
    ios_base::sync_with_stdio(false);
    cin.tie(nullptr); cout.tie(nullptr);

    cout<<fixed<<setprecision(10);

    int N; cin>>N;
    vector<int> top(4, 0);
    for(int i(0); i<=N-1; ++i){
        int A; cin>>A;
        auto it(upper_bound(top.begin(), top.end(), A));
        if(it==top.begin()){
            cout<<"NO";
            return 0;
        }
        *prev(it)=A;
    }
    cout<<"YES";
    return 0;
}